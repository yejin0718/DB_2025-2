--주문테이블에서 p03 또는 p06의 제품을 주문하고 배송지가 경기도인 행을 검색하시오. 단 주문수량을 기준으로 내림차순 정렬하시오.
select *
from 주문
where 주문제품 in ('p03', 'p06') and 배송지 like '경기도%'
order by 수량 desc;

--주문테이블에서 수량이 10개 이상인 주문의 주문고객, 주문제품, 수량, 주문일자를 검색하시오. 단, 주문제품을 기준으로 오름차순 정렬하고, 동일 제품은 수량을 기준으로 내림차순 정렬하시오.
select 주문고객, 주문제품, 수량, 주문일자
from 주문
where 수량 >= 10
order by 주문제품 asc, 수량 desc;

--주문테이블에서 a 또는 b로 시작하고 수량이 10개 이상인 주문의 주문고객, 주문제품, 수량, 배송지를 검색하시오. 
--단, 주문고객을 기준으로 내림차순 정렬하고 동일 주문고객은 주문제품을 기준으로 내림차순 정렬하시오
select 주문고객, 주문제품, 수량, 배송지
from 주문
where 수량 >= 10 and (주문고객 like 'a%' or 주문고객 like 'b%')
order by 주문고객 desc, 주문제품 desc;

