--고객테이블에서 성이 김씨인 고객의 고객이름, 나이, 등급을 검색하시오.
select 고객이름, 나이, 등급
from 고객
where 고객이름 like '김%';

--고객테이블에서 등급에 i문자가 포함된 고객의 고객이름, 등급, 직업을 검색하시오.
select 고객이름, 등급, 직업
from 고객
where 등급 LIKE '%i%';

--고객테이블에서 고객아이디에 a문자가 포함된 고객의 고객아이디, 고객이름, 등급, 직업을 검색하시오.
select 고객아이디, 고객이름, 등급, 직업
from 고객
where 고객아이디 like '%a%';

--고객테이블에서 고객아이디에 a문자가 포함되어 있고 등급에 i문자가 포함된 고객의 고객아이디, 고객이름, 등급, 직업을 검색하시오.
select 고객아이디, 고객이름, 등급, 직업
from 고객
where (고객이름 like '김%') and (고객아이디 like '%a%') and (등급 like '%i%');

--고객 테이블에서 고객아이디가 5자인 고객의 고객아이디, 나이, 등급, 직업, 적립급을 검색하시오.
select 고객아이디, 나이, 등급, 직업, 적립금
from 고객
where 고객아이디 like '_____';

--고객테이블에서 고객아이디가 5글자이고 적립금이 3000원 이상인 고객의 고객아이디, 나이, 등급, 직업, 적립금을 검색하시오.
select 고객아이디, 나이, 등급, 직업, 적립금
from 고객
where 고객아이디 like '_____' and 적립금 > 3000;

--고객테이블에서 직업명이 '사'자로 끝나는 고객의 고객이름, 직업, 등급을 검색하시오.
select 고객이름, 직업, 등급
from 고객
where 직업 like '%사';

--고객테이블에서 직업명이 '사'자로 끝나면서 2글자인 고객의 고객이름, 직업, 등급을 검색하시오.
select 고객이름, 직업, 등급
from 고객
where 직업 like '%사' and 직업 like '__';

--고객 테이블에서 나이가 아직 입력되지 않은 고객의 고객이름을 검색하시오.
select 고객이름
from 고객
where 나이 is null;

--고객테이블에서 직업명이 '사'자로 끝나는 고객의 고객이름, 직업, 등급을 검색하시오.
select 고객이름, 직업, 등급
from 고객
where 직업 like '%사';

--고객테이블에서 나이가 25세 미만인 고객의 고객이름, 나이를 검색하시오.
--입력된 값이 없는 컬럼(필드)은 비교연산 결과가 false라서 검색이 되지 않는다.
select 고객이름, 나이
from 고객
where 나이 < 25 ;

--고객테이블에서 나이가 25세 미만인 고객의 고객이름, 나이를 검색하시오.
select 고객이름, 나이
from 고객
where 나이 >= 25 ;

--*** order by절 (정렬)
--asc(오름차순: 작은 값 => 큰 값), desc(내림차순: 큰 값 => 작은 값)
--null: 오름차순 맨뒤, 내림차순 맨처음

--고객테이블에서 고객이름, 나이를 검색하시오. 단 나이를 기준으로 오름차순 정렬하시오.
select 고객이름, 나이
from 고객
order by 나이 asc;

--고객테이블에서 고객이름, 나이를 검색하시오. 단 나이를 기준으로 내림차순 정렬하시오.
select 고객이름, 나이
from 고객
order by 나이 desc;

--고객테이블에서 등급이 gold 또는 vip인 고객의 고객이름, 나이, 등급, 직업을 검색하시오. 단, 고객의 등급을 기준으로 오름차순 정렬하시오.
select 고객이름, 나이, 등급, 직업
from 고객
where 등급 = 'gold' or 등급 = 'vip'
order by 등급;

